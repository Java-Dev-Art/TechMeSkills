package com.tms.model;

public class Calculator {
    public int sum(int a, int b) {
        return a + b;
    }

    public int increase(int a, int b) {
        return a * b;
    }

    public double division(int a, int b) {
        return (double) a / b;
    }

    public int subtraction(int a, int b) {
        return a - b;
    }
}
